/*
ASSIGNMENT OPERATORS
*/
let r = 5
console.log(r);
// our equal sign is the assignment

//addition assignment
let r = 5
r += 1; //r= r+1 or 5 + 1 in this case
console.log(r);

//subtraction

let r = 4
r -= 2; // r = r - 2
console.log(r);

//multiplication
let r = 4
r *= 2; //r = r * 2
console.log(r);

//division
let r = 7
r /= 2; //r = r/1
console.log(r);

//EXPONENTIAL ASSIGNMENT
let r = 2
r **= 5; //r = r
console.log(r); // r * r * r

//REMAINDER (MODULUS) ASSIGNment

r %= 1; //r = r % 1
console.log(r);

let r = 100
r %= 13; //r = r % 13
console.log(r);

let y = 5;
console.log(y);
y %= 2;
console.log(y);